package Main;

public class Pixel {
    private int ID, x,y;

    public Pixel(int ID,int x, int y) {
        this.x = x;
        this.y = y;
        this.ID = ID;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
